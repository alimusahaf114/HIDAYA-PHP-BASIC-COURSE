    <h1 id="intro">Introduction to Space Exploration</h1>
    <p>
        Space exploration is the investigation of outer space through the use of astronomy and space technology.
        It began in earnest during the mid-20th century, driven by the Cold War rivalry between the United States
        and the Soviet Union. The Soviet Union launched the first artificial satellite, Sputnik 1, in 1957, marking
        the dawn of the Space Age. Since then, dozens of countries and private companies have contributed to our
        understanding of the cosmos through robotic probes, crewed missions, and powerful observatories. Space
        exploration has yielded countless technological spinoffs and expanded our understanding of Earth's place
        in the universe.
    </p>

    <hr>

    <h1 id="moon">Moon Landings</h1>
    <p>
        The Apollo program, run by NASA, successfully landed humans on the Moon six times between 1969 and 1972.
        Apollo 11 made the first crewed lunar landing on July 20, 1969, when astronauts Neil Armstrong and
        Buzz Aldrin walked on the lunar surface while Michael Collins orbited above. In total, twelve astronauts
        walked on the Moon during the Apollo missions, collecting 382 kilograms of lunar rock and soil samples.
        These missions provided invaluable data about the Moon's geology and origin, and demonstrated that humans
        could survive and work in the harsh environment of space. Plans for a return to the Moon under NASA's
        Artemis program are currently underway.
    </p>

    <hr>

    <h1 id="mars">Mars Missions</h1>
    <p>
        Mars has been the target of numerous robotic missions since the 1960s. NASA's Mars rovers — including
        Sojourner, Spirit, Opportunity, Curiosity, and Perseverance — have traversed the Martian surface,
        analysing rocks and searching for signs of ancient microbial life. The Perseverance rover, which landed
        in Jezero Crater in February 2021, also carried the Ingenuity helicopter, the first powered aircraft to
        fly on another planet. China's Tianwen-1 mission successfully deployed the Zhurong rover in 2021 as well.
        Future crewed missions to Mars are being planned by both NASA and SpaceX, with the goal of establishing
        a permanent human presence on the Red Planet.
    </p>

    <hr>

    <h1 id="telescopes">Space Telescopes</h1>
    <p>
        Space telescopes orbit above Earth's atmosphere, allowing them to observe the universe without atmospheric
        distortion. The Hubble Space Telescope, launched in 1990, has produced some of the most iconic images
        in astronomy and helped determine the age of the universe at approximately 13.8 billion years. The James
        Webb Space Telescope, launched in December 2021, is the most powerful space telescope ever built. It
        observes in infrared light, allowing it to peer through dust clouds and observe the earliest galaxies
        formed after the Big Bang. Other notable missions include the Chandra X-ray Observatory and the
        Spitzer Space Telescope, each revealing different layers of the cosmos.
    </p>

    <hr>

    <h1 id="future">Future of Space Travel</h1>
    <p>
        The future of space travel is being shaped by both government agencies and private companies. SpaceX,
        founded by Elon Musk, has revolutionised the industry with reusable rockets like the Falcon 9 and is
        developing the fully reusable Starship vehicle intended for missions to the Moon and Mars. Blue Origin,
        founded by Jeff Bezos, is working on its New Glenn rocket and lunar lander systems. NASA's Artemis program
        aims to return astronauts to the Moon by the mid-2020s and establish a sustainable lunar presence as a
        stepping stone to Mars. Beyond our solar system, concepts like solar sails and nuclear propulsion are
        being studied to make interstellar travel a long-term possibility for humanity.
    </p>

