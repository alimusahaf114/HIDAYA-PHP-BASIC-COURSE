    <h1 id="intro">Introduction to Artificial Intelligence</h1>
    <p>
        Artificial Intelligence (AI) refers to the simulation of human intelligence processes by computer systems.
        These processes include learning, reasoning, and self-correction. AI was formally founded as an academic
        discipline in 1956 at the Dartmouth Conference. Today AI powers everything from search engines and
        recommendation systems to medical diagnostics and autonomous vehicles. The field is broadly divided into
        Narrow AI, which excels at a single task, and the still-theoretical General AI, which would match or
        exceed human cognitive abilities across the board.
    </p>

    <hr>

    <h1 id="ml">Machine Learning</h1>
    <p>
        Machine Learning (ML) is a subset of AI that gives systems the ability to learn and improve from experience
        without being explicitly programmed. Instead of writing rigid rule-based code, developers feed data to
        algorithms that detect patterns and make decisions with minimal human intervention. Supervised learning,
        unsupervised learning, and reinforcement learning are its three main paradigms. Applications range from
        spam detection and credit scoring to medical image analysis and natural language understanding.
    </p>

    <hr>

    <h1 id="neural">Neural Networks and Deep Learning</h1>
    <p>
        Neural networks are computing systems inspired by the biological neural networks that constitute animal brains.
        They consist of layers of interconnected nodes or neurons that process data using connectionist approaches.
        Deep Learning uses neural networks with many layers to model complex patterns in data. Convolutional Neural
        Networks (CNNs) excel at image recognition, while Recurrent Neural Networks (RNNs) and Transformers dominate
        natural language tasks. Landmark models like GPT, BERT, and ResNet have demonstrated that deep learning can
        surpass human-level performance on specific benchmarks.
    </p>

    <hr>

    <h1 id="nlp">Natural Language Processing</h1>
    <p>
        Natural Language Processing (NLP) is the branch of AI concerned with enabling computers to understand,
        interpret, and generate human language. Core NLP tasks include tokenisation, part-of-speech tagging,
        named-entity recognition, sentiment analysis, machine translation, and question answering. The introduction
        of the Transformer architecture in 2017 revolutionised the field, leading to large language models that can
        generate coherent long-form text, write code, and converse naturally with humans.
    </p>

    <hr>

    <h1 id="ethics">AI Ethics and the Future</h1>
    <p>
        As AI systems grow more capable, society faces profound ethical and governance challenges. Key concerns include
        algorithmic bias, lack of transparency in black-box models, privacy erosion, labour displacement, and
        long-term risks from highly capable systems. Regulatory frameworks are emerging worldwide, such as the EU AI Act,
        which categorises AI by risk level and imposes strict obligations on high-risk applications. Researchers in
        AI Safety work on alignment techniques and interpretability to ensure powerful AI systems remain beneficial.
    </p>

